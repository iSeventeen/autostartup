package com.android.autostartup.model;

public class Parent {

    public long id;
    public String name;
    public String role;
    public String avatar;
    public long student;

    public Parent(long id, String name, String role, String avatar, long student) {
        this.id = id;
        this.name = name;
        this.role = role;
        this.avatar = avatar;
        this.student = student;
    }

}
